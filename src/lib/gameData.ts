export const getGameById = (id: string): Game | undefined => {
  return games.find(game => game.id === id);
};

export const getGameByTitle = (title: string): Game | undefined => {
  return games.find(game => game.title.toLowerCase() === title.toLowerCase());
};

export const getOtherGames = (currentId: string): Game[] => {
  return games.filter(game => game.id !== currentId);
};

export const searchGames = (query: string): Game[] => {
  const lowerCaseQuery = query.toLowerCase();
  return games.filter(game => 
    game.title.toLowerCase().includes(lowerCaseQuery) || 
    game.description.toLowerCase().includes(lowerCaseQuery)
  );
};
